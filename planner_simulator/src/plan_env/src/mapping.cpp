#include "plan_env/sdf_map.h"
#include <ros/ros.h>

using namespace std;
using namespace flyco;
using flyco::SDFMap;

int main(int argc, char** argv) 
{
  ros::init(argc, argv, "dual_mapping");
  ros::NodeHandle nh("~");

  std::shared_ptr<SDFMap> sdf_map(new SDFMap);
  sdf_map->setXYoffset(-2.577, -1.0222);
  sdf_map->initMap(nh);
  sdf_map->initHC(nh);

  ros::Duration(1.0).sleep();
  ros::spin();

  return 0;
}