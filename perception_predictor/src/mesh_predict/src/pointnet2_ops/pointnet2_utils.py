def _load_extension_from_path(extension_path: Path):
    spec = importlib.util.spec_from_file_location(
        "pointnet2_ops._ext", extension_path
    )
    if spec is None or spec.loader is None:
        return None

    try:
        module = importlib.util.module_from_spec(spec)
        sys.modules[spec.name] = module
        spec.loader.exec_module(module)
    except (ImportError, OSError) as exc:
        sys.modules.pop(spec.name, None)
        warnings.warn(
            "Skipping incompatible pointnet2_ops CUDA extension "
            f"{extension_path}: {exc}"
        )
        return None
    return module


def _iter_explicit_extension_candidates():
    current_package_dir = Path(__file__).resolve().parent
    explicit_path = os.environ.get("POINTNET2_OPS_EXTENSION_PATH", "").strip()
    if explicit_path:
        yield Path(explicit_path).expanduser().resolve()

    # Only trust binaries colocated with the current vendored package.
    for extension_path in sorted(current_package_dir.glob("_ext*.so")):
        yield extension_path.resolve()

    torch_extensions_dir = Path(
        os.environ.get(
            "TORCH_EXTENSIONS_DIR",
            Path.home() / ".cache" / "torch_extensions",
        )
    ).expanduser()
    if torch.version.cuda:
        cuda_version = torch.version.cuda.replace(".", "")
        cache_key = f"py{sys.version_info.major}{sys.version_info.minor}_cu{cuda_version}"
        yield torch_extensions_dir / cache_key / "_ext" / "_ext.so"


def _load_prebuilt_extension():
    """Load only explicitly approved or colocated pointnet2_ops extensions."""

    for extension_path in _iter_explicit_extension_candidates():
        if not extension_path.is_file():
            warnings.warn(
                "Skipping missing pointnet2_ops CUDA extension "
                f"{extension_path}."
            )
            continue

        module = _load_extension_from_path(extension_path)
        if module is None:
            continue

        warnings.warn(
            "Loaded pointnet2_ops CUDA extension from "
            f"{extension_path}."
        )
        return module

    return None


_ext = _load_prebuilt_extension()

